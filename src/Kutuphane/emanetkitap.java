    private void txtidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String uid = txtid.getText();
            try {
                pst = con.prepareStatement("select * from uye where id=?");
                pst.setString(1, uid);
                rs = pst.executeQuery();
                
                if (rs.next()== false)
                {
                    JOptionPane.showMessageDialog(this, "UYE ID Bulunamadı");
                }
                else
                {
                    String uyeadi = rs.getString("isim");
                    txtuye.setText (uyeadi.trim());
                    
                            
                }
                
                
                
                
                
                
            } catch (SQLException ex) {
                Logger.getLogger(emanetkitap.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
    }//GEN-LAST:event_txtidKeyPressed

