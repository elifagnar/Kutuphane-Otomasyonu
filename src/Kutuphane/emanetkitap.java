    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String uid = txtid.getText();
        KitapItem pitem = (KitapItem) txtkitap.getSelectedItem();
        
        SimpleDateFormat date_format = new SimpleDateFormat ("yyyy-MM-dd");
        String kitaptarihi = date_format.format(txtkitaptarihi.getDate());
        
        SimpleDateFormat date_format1 = new SimpleDateFormat ("yyyy-MM-dd");
        String iadetarihi = date_format.format(txtitarih.getDate());
        
  
        try {
            pst = con.prepareStatement("insert into emanetkitap(uyeid,kitapid,yayıntarihi,iadetarihi)values (?,?,?,?)");
        
            pst.setString(1,uid);
            pst.setInt(2, pitem.id);
            pst.setString(3, kitaptarihi);
            pst.setString(4, iadetarihi);
            int k = pst.executeUpdate();

            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "Emanet Kitap Oluştur");

                txtid.setText("");
                txtkitap.setSelectedIndex(-1);
                txtuye.setText("");
                Kitapyayını_Load();
                
                
                
            // Yayımcı_Load();

            }
            else
            {
                JOptionPane.showMessageDialog(this, "Hatalı");
            }

        } catch (SQLException ex) {
            Logger.getLogger(emanetkitap.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

