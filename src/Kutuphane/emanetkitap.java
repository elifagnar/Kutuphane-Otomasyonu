    public void kitap()
    {
        try {
            pst = con.prepareStatement("select * from kitap");
             rs = pst.executeQuery();
             txtkitap.removeAllItems();
             
             while (rs.next())
             {
                 txtkitap.addItem(new KitapItem(rs.getInt(1),rs.getString(2)));
                 
             }
             
        } catch (SQLException ex) {
            Logger.getLogger(Kitap.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    
    
   
    public void Kitapyayını_Load()
    {
        int c;
        try {
            pst = con.prepareStatement("select e.id,u.isim,p.kadi,e.yayıntarihi,e.iadetarihi from emanetkitap e JOIN uye u ON e.uyeid = u.id JOIN kitap p ON e.kitapid = p.id");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for (int i=1; i<=c; i++)
                {
                    v2.add(rs.getString("e.id"));
                    v2.add(rs.getString("u.isim"));
                    v2.add(rs.getString("p.kadi"));
                    v2.add(rs.getString("e.yayıntarihi"));  
                    v2.add(rs.getString("e.iadetarihi"));
                   
                }
                
                d.addRow(v2);
              
            }
          
        } catch (SQLException ex) {
            Logger.getLogger(emanetkitap.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
